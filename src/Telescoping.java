public class Telescoping {

    int a;
    int b;
    int c;
    int d;
    int e;

    public Telescoping(int a) {
        this.a = a;
    }

    public Telescoping(int b, int c) {
        this.b = b;
        this.c = c;
    }

    public Telescoping(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    // list goes on.
}
